    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
        // Lấy dữ liệu từ các ô nhập
        int maSP = Integer.parseInt(txtMaSanPham.getText().trim());
        String tenSP = txtTenSanPham.getText().trim();
        BigDecimal donGia = new BigDecimal(txtDonGia.getText().trim());
        int maLoai = Integer.parseInt(txtMaLoai.getText().trim());
        String donViTinh = "Chiếc"; // Mặc định

        // Kiểm tra nếu không thay đổi ảnh, giữ ảnh cũ
        String hinhAnh = (this.HinhAnh == null || this.HinhAnh.isEmpty()) ? getCurrentImagePath(maSP) : this.HinhAnh;

        // Tạo đối tượng sản phẩm
        SanPhamDTO sp = new SanPhamDTO(maSP, tenSP, 0, donGia, donViTinh, hinhAnh, maLoai);
        SanPhamBUS spBUS = new SanPhamBUS();
        boolean updatedSP = spBUS.update(sp);

        // Chi tiết sản phẩm
        int maHang = Integer.parseInt(txtMaHang.getText().trim());
        String chip = txtChip.getText().trim();
        String ram = txtRam.getText().trim();
        String rom = txtRom.getText().trim();
        String inch = txtInch.getText().trim();
        String dungLuongPin = txtDungLuongPin.getText().trim();
        String mauSac = txtMauSac.getText().trim();

        ChiTietSanPhamDTO ctsp = new ChiTietSanPhamDTO(maSP, maHang, chip, ram, rom, inch, dungLuongPin, mauSac);
        ChiTietSanPhamBUS ctspBUS = new ChiTietSanPhamBUS();
        boolean updatedCTSP = ctspBUS.update(ctsp);

        // Kết quả
        if (updatedSP && updatedCTSP) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            mainForm.loadSanPhamToTable();
            this.dispose(); // Đóng form
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

